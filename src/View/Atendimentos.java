    private void jbEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEditarActionPerformed
        CadastroServicosDAO servico = new CadastroServicosDAO();
        CadastroServicosBeans editar = new CadastroServicosBeans();

        editar.setIdServico(Integer.parseInt(jtfCodigoAtendimentos.getText()));

        try {
            servico.alterar(editar);
        } catch (SQLException ex) {
            Logger.getLogger(Atendimentos.class.getName()).log(Level.SEVERE, null, ex);
        }

        limpaCampos();

        try {
            CadastroServicosDAO.listaServicos();
        } catch (SQLException ex) {
            Logger.getLogger(Atendimentos.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jbEditarActionPerformed

