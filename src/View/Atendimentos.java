    public Atendimentos() throws SQLException {
        
        listaServicos = CadastroServicosDAO.listaServicos();
        listaClientes = CadastroClientesDAO.listaClientes();
        listaColaboradores = CadastroColaboradoresDAO.listaColaboradores();
        
        modelServicos = new DefaultComboBoxModel<>();
        for (CadastroServicosBeans servico : listaServicos) {
            modelServicos.addElement(servico);
        }
        modelClientes = new DefaultComboBoxModel<>();
        for (CadastroClientesBeans clientes : listaClientes) {
            modelClientes.addElement(clientes);
        }
        modelColaboradores = new DefaultComboBoxModel<>();
        for (CadastroColaboradoresBeans colaboradores : listaColaboradores) {
            modelColaboradores.addElement(colaboradores);
        }

        listaAtendimentos = CadastroAtendimentosDAO.listaAtendimentos();

        modelAtendimentos = new DefaultTableModel(new String[]{
            "Código", "Nome Serviço", "Nome Cliente", "Nome Colaborador", "Data Atendimento"
        }, 0);

        for (CadastroAtendimentosBeans atendimento : listaAtendimentos) {
            String nomeServico = "";
            String nomeCliente = "";
            String nomeColaborador = "";
            for (CadastroServicosBeans servico: listaServicos){
                if (servico.getIdServico() == atendimento.getIdServico()){
                    nomeServico = servico.getNome();
                    break;
                }
            }
            
            for (CadastroClientesBeans cliente: listaClientes){
                if (cliente.getIdClientes() == atendimento.getIdClientes()){
                    nomeCliente = cliente.getNomeClientes();
                    break;
                }
            }
            
            for (CadastroColaboradoresBeans colaborador: listaColaboradores){
                if (colaborador.getIdColaboradores()== atendimento.getIdColaboradores()){
                    nomeColaborador = colaborador.getNomeColaboradores();
                    break;
                }
            }
            
            Object[] linha = {
                atendimento.getIdAtendimentos(),
                nomeServico,
                nomeCliente,
                nomeColaborador,
                atendimento.getDataAtendimentos()
            }; 
            modelAtendimentos.addRow(linha);
        }

        initComponents();

    }

